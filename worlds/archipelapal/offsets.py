# Junk Item
# LE Byte 0 = Offset
# LE Byte 1 = Theme
# LE Byte 2 = Unique Name
JUNK_ID_OFFSET = 0x000000

# Goal Item
# LE Byte 0 = Offset
# LE Byte 1 = Theme
# LE Byte 2 = Unique Name
GOAL_ID_OFFSET = 0x010000

# Key Item
# LE Byte 0 = Offset
# LE Byte 1 = Theme
# LE Byte 2 = Region
KEY_ID_OFFSET = 0x020000

# Chest ID offset
# LE Byte 0 = Offset
# LE Byte 1 = Region
# LE Byte 2 = Theme
# LE Byte 3 = Chest Number
CHEST_ID_OFFSET = 0x03000000

# Game-affecting Items
# LE Byte 0 = Offset
# LE Byte 1 = Affect Type
# LE Byte 2 = Theme
# LE Byte 3 = Unique Name
GAME_AFFECTOR_ID_OFFSET = 0x04000000
